#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	size;
	char	*res;

	size = ft_strlen(s);
	if (!s || size < start)
		return (0);
	len = (start + len < size) ? len : size - start;
	if (!(res = (char *)malloc((len + 1) * sizeof(char))))
		return (0);
	i = 0;
	while (s[start + i] && i < len)
	{
		res[i] = s[start + i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
